@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class UserDto
{
    private Long id;
    @NotEmpty
    private String firstName;
    @NotEmpty
    private String lastName;
    @NotEmpty(message = "Dopl≈àte e-mail.")
    @Email
    private String email;
    @NotEmpty(message = "Zadejte heslo.")
    private String password;
}
